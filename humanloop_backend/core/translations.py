def get_translations(language_code: str) -> dict:
    """Return the translation dict for a given language code.
    Merges with English as base so no key is ever missing."""
    base = dict(TRANSLATIONS['en'])  # start with English
    if language_code and language_code != 'en' and language_code in TRANSLATIONS:
        base.update(TRANSLATIONS[language_code])
    return base
